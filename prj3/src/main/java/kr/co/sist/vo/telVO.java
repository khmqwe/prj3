package kr.co.sist.vo;

public class telVO {

	public String inputTel1,inputTel2,inputTel3;

	
	public telVO() {
	}


	public telVO(String inputTel1, String inputTel2, String inputTel3) {
		this.inputTel1 = inputTel1;
		this.inputTel2 = inputTel2;
		this.inputTel3 = inputTel3;
	}


	public String getInputTel1() {
		return inputTel1;
	}


	public String getInputTel2() {
		return inputTel2;
	}


	public String getInputTel3() {
		return inputTel3;
	}


	public void setInputTel1(String inputTel1) {
		this.inputTel1 = inputTel1;
	}


	public void setInputTel2(String inputTel2) {
		this.inputTel2 = inputTel2;
	}


	public void setInputTel3(String inputTel3) {
		this.inputTel3 = inputTel3;
	}
	
	
	
	
}
