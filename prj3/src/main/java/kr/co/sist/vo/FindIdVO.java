package kr.co.sist.vo;

public class FindIdVO {
	private String m_name,m_telnum;

	public FindIdVO() {
	}

	public FindIdVO(String m_name, String m_telnum) {
		this.m_name = m_name;
		this.m_telnum = m_telnum;
	}

	public String getM_name() {
		return m_name;
	}

	public String getM_telnum() {
		return m_telnum;
	}

	public void setM_name(String m_name) {
		this.m_name = m_name;
	}

	public void setM_telnum(String m_telnum) {
		this.m_telnum = m_telnum;
	}


	
	
	
}
